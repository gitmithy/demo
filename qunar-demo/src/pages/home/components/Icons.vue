<template>
  <div class="icons">
    <swiper  :options="swiperOption">
      <!-- slides -->
      <swiper-slide v-for="(page,index) of pages" :key="index">
        <div
        class="icon"
        v-for="item of page"
        :key="item.id"
        >
          <div class="icon-img">
            <img
              class="icon-img-content"
              :src='item.imgUrl'
              alt
              srcset
            />
          </div>
          <p class="icon-desc">{{item.desc}}</p>
        </div>
      </swiper-slide>

      <!-- <div class="swiper-pagination2" slot="pagination2"></div> -->
    </swiper>
  </div>
</template>
<script>
export default {
  name: "HomeIcons",
  props:{
    list:Array
  },
  data(){
    return{
       swiperOption:{
         autoplay:false
       }
    }
  },
  computed:{
    pages(){
      const pages=[]
      this.list.forEach((item,index)=>{
        const page=Math.floor(index/8)
        if(!pages[page]){
          pages[page]=[]
        }
        pages[page].push(item)
      })
      return pages
    }
  },



};
</script>
